class AnagramChecker:
    """
    A class to check for valid words and find anagrams.
    """
    
    def __init__(self, word_list_text=None):
        """
        Initialize the AnagramChecker with a word list.
        If no word list is provided, uses the built-in list.
        """
        if word_list_text is None:
            word_list_text = BUILTIN_WORD_LIST
        
        # Load words into a set for O(1) lookup, all lowercase
        self.words = set()
        for line in word_list_text.strip().split('\n'):
            word = line.strip().lower()
            if word and word.isalpha():
                self.words.add(word)
    
    def is_valid_word(self, word):
        """
        Check if the given word exists in the loaded word list.
        Case-insensitive comparison.
        """
        return word.lower() in self.words
    
    def is_anagram(self, word1, word2):
        """
        Check if word1 and word2 are anagrams (same letters, different order).
        Returns True if anagrams, False otherwise.
        """
        # Convert to lowercase and sort characters
        w1_sorted = sorted(word1.lower())
        w2_sorted = sorted(word2.lower())
        return w1_sorted == w2_sorted
    
    def get_anagrams(self, word):
        """
        Find all anagrams for the given word from the word list.
        Returns a list of anagrams (excluding the word itself).
        """
        anagrams = []
        word_lower = word.lower()
        
        for dict_word in self.words:
            # Skip if it's the same word
            if dict_word == word_lower:
                continue
            # Check if it's an anagram
            if self.is_anagram(word_lower, dict_word):
                anagrams.append(dict_word)
        
        return sorted(anagrams)


def get_user_input():
    """
    Get and validate user input.
    Returns the validated word or None if user wants to exit.
    """
    user_input = input("\nEnter a word (or 'exit' to quit): ").strip()
    
    # Check for exit
    if user_input.lower() == 'exit':
        return None
    
    # Check for empty input
    if not user_input:
        print("❌ Error: You entered nothing. Please try again.")
        return False
    
    # Check for multiple words (contains whitespace after stripping)
    if len(user_input.split()) > 1:
        print("❌ Error: Only a single word is allowed. Please try again.")
        return False
    
    # Check for alphabetic characters only
    if not user_input.isalpha():
        print("❌ Error: Only alphabetic characters are allowed (no numbers or special characters).")
        return False
    
    return user_input


def display_results(word, checker):
    """
    Display the word information in a formatted message.
    """
    word_upper = word.upper()
    
    print("\n" + "=" * 50)
    print(f'YOUR WORD: "{word_upper}"')
    print("=" * 50)
    
    # Check if valid
    is_valid = checker.is_valid_word(word)
    if is_valid:
        print("✅ This is a valid English word.")
    else:
        print("❌ This is NOT a valid English word (not in our dictionary).")
    
    # Get anagrams
    anagrams = checker.get_anagrams(word)
    
    if anagrams:
        print(f"\n🎯 Anagrams for your word: {', '.join(anagrams)}")
    else:
        print("\n🚫 No anagrams found for this word.")
    
    print("=" * 50)


def main():
    """
    Main function - runs the anagram checker program.
    """
    print("╔══════════════════════════════════════════════════════════╗")
    print("║           🔤 ANAGRAM CHECKER 🔤                          ║")
    print("║                                                          ║")
    print("║  Find all possible anagrams for any English word!        ║")
    print("║  Type 'exit' at any time to quit.                        ║")
    print("╚══════════════════════════════════════════════════════════╝")
    
    # Initialize the AnagramChecker
    print("\n📚 Loading word list...")
    checker = AnagramChecker()
    print(f"✅ Loaded {len(checker.words)} words into the dictionary.")
    
    # Main menu loop
    while True:
        # Get user input
        result = get_user_input()
        
        # Check if user wants to exit
        if result is None:
            print("\n👋 Thank you for using Anagram Checker. Goodbye!")
            break
        
        # Check if input was invalid (False returned)
        if result is False:
            continue
        
        # Valid word entered - display results
        display_results(result, checker)


if __name__ == "__main__":
    main()